import { notificationController } from "../controllers/notificationController"

const endpoint = "notifications"

export const setNotificationRouting = (app) => {
    app.get(`/${endpoint}`, notificationController.findAll)
    app.get(`/${endpoint}/:id`, notificationController.findById)
    app.post(`/${endpoint}`, notificationController.create)
    app.put(`/${endpoint}/:id`, notificationController.update)
    app.delete(`/${endpoint}/:id`, notificationController.delete)
}
